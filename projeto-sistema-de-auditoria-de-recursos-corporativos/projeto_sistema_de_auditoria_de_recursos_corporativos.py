import time

# ========================
# Decorator de Auditoria
# ========================
def auditor(func):
    """
    Decorator que imprime informações de auditoria antes e depois da execução da função.
    Mede o tempo de execução e exibe os argumentos passados.
    """
    def wrapper(*args, **kwargs):
        print("========== INÍCIO DA AUDITORIA ==========")
        print(f"Função chamada: {func._name_}")
        print(f"Argumentos posicionais: {args}")
        print(f"Argumentos nomeados: {kwargs}")
        start_time = time.time()
        result = func(*args, **kwargs)
        end_time = time.time()
        print(f"Tempo de execução: {end_time - start_time:.6f} segundos")
        print("=========== FIM DA AUDITORIA ===========\n")
        return result
    return wrapper

