# ========================
# Função Recursiva de Cálculo de Orçamento
# ========================
@auditor
def calcular_orcamento(dicionario, *ignore_departamentos, **kwargs):
    """
    Calcula o orçamento total da empresa de forma recursiva.

    Parâmetros:
    - dicionario: Estrutura de dados aninhada (dict)
    - *ignore_departamentos: lista de departamentos a serem ignorados na soma
    - **kwargs: parâmetros adicionais, como moeda_destino e taxa_cambio
    """
    taxa_cambio = kwargs.get("taxa_cambio", 1)  # Default = 1 se não for fornecida
    moeda_destino = kwargs.get("moeda_destino", "USD")

    def soma_orcamento(dic):
        total = 0
        for chave, valor in dic.items():
            # Ignora departamento e todos os sub-departamentos
            if chave in ignore_departamentos:
                continue
            if isinstance(valor, dict):
                total += soma_orcamento(valor)
            else:
                total += valor
        return total

    total = soma_orcamento(dicionario)
    total_convertido = total * taxa_cambio
    print(f"Orçamento total em {moeda_destino}: {total_convertido}")
    return total_convertido

